// JavaScript Assignment Operators Example

let x = 10;
console.log("Initial value of x:", x);

// Simple assignment
x = 15;
console.log("x = 15 ➜", x);

// Addition assignment
x += 5; // same as x = x + 5
console.log("x += 5 ➜", x);

// Subtraction assignment
x -= 3; // same as x = x - 3
console.log("x -= 3 ➜", x);

// Multiplication assignment
x *= 2; // same as x = x * 2
console.log("x *= 2 ➜", x);

// Division assignment
x /= 4; // same as x = x / 4
console.log("x /= 4 ➜", x);

// Modulus assignment
x %= 3; // same as x = x % 3
console.log("x %= 3 ➜", x);
