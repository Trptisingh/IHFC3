// JavaScript Logical Operators Example

let a = true;
let b = false;

console.log("a =", a, ", b =", b);

// Logical AND (&&)
console.log("a && b ➜", a && b); // false — both must be true
console.log("a && true ➜", a && true); // true

// Logical OR (||)
console.log("a || b ➜", a || b); // true — at least one must be true
console.log("b || false ➜", b || false); // false

// Logical NOT (!)
console.log("!a ➜", !a); // false — reverses the value
console.log("!b ➜", !b); // true
